<#
.SYNOPSIS
    Validates the structure and content of ClassDocContent.psd1.

.DESCRIPTION
    Test-ClassDocContent.ps1 validates that the ClassDocContent.psd1 file
    has the correct structure and data types.
    
    Checks:
    - File can be imported
    - _Metadata section exists and is valid
    - Required keys exist for each class/enum
    - Data types are correct
    - No invalid keys

.PARAMETER Path
    Path to ClassDocContent.psd1. Defaults to Build\ClassDocContent.psd1

.PARAMETER Detailed
    Show detailed validation results for each class.

.EXAMPLE
    .\Build\Test-ClassDocContent.ps1
    Validates the default ClassDocContent.psd1 file.

.EXAMPLE
    .\Build\Test-ClassDocContent.ps1 -Detailed
    Shows detailed validation for each class.

.NOTES
    Author: Robert Faddes
    Run this after manually editing ClassDocContent.psd1 to ensure structure is valid.
#>
[CmdletBinding()]
param(
    [string]$Path,
    [switch]$Detailed
)

$ErrorActionPreference = 'Stop'

# Get module root and default path
$ModuleRoot = Split-Path $PSScriptRoot -Parent
if (-not $Path) {
    $Path = Join-Path $PSScriptRoot 'ClassDocContent.psd1'
}

if (-not (Test-Path $Path)) {
    Write-Error "File not found: $Path"
    return
}

Write-Host "`n=== Test-ClassDocContent ===" -ForegroundColor Cyan
Write-Host "Validating: $Path" -ForegroundColor Yellow

$ValidationErrors = @()
$ValidationWarnings = @()

# Test 1: Can import the file
Write-Host "`nTest 1: Importing file..." -ForegroundColor Yellow
try {
    $DocContent = Import-PowerShellDataFile -Path $Path
    Write-Host "  PASS: File imported successfully" -ForegroundColor Green
} catch {
    Write-Host "  FAIL: Could not import file" -ForegroundColor Red
    Write-Host "  Error: $($_.Exception.Message)" -ForegroundColor Red
    $ValidationErrors += "Cannot import PSD1 file: $($_.Exception.Message)"
    return  # Can't continue if import fails
}

# Test 2: Metadata exists and is valid
Write-Host "`nTest 2: Validating _Metadata..." -ForegroundColor Yellow
if ($DocContent.ContainsKey('_Metadata')) {
    $Meta = $DocContent['_Metadata']
    $MetaValid = $true
    
    if (-not $Meta.ContainsKey('Version')) {
        $ValidationWarnings += "_Metadata missing 'Version' key"
        $MetaValid = $false
    }
    if (-not $Meta.ContainsKey('LastUpdated')) {
        $ValidationWarnings += "_Metadata missing 'LastUpdated' key"
        $MetaValid = $false
    }
    if (-not $Meta.ContainsKey('AutoGenerated')) {
        $ValidationWarnings += "_Metadata missing 'AutoGenerated' key"
        $MetaValid = $false
    }
    
    if ($MetaValid) {
        Write-Host "  PASS: _Metadata is valid" -ForegroundColor Green
    } else {
        Write-Host "  WARN: _Metadata has warnings" -ForegroundColor Yellow
    }
} else {
    $ValidationWarnings += "_Metadata section missing"
    Write-Host "  WARN: _Metadata section missing" -ForegroundColor Yellow
}

# Test 3: Validate structure of each region/class
Write-Host "`nTest 3: Validating region/class structure..." -ForegroundColor Yellow
$ClassCount = 0
$EnumCount = 0

foreach ($RegionKey in $DocContent.Keys) {
    if ($RegionKey -eq '_Metadata') { continue }
    
    if ($DocContent[$RegionKey] -isnot [hashtable]) {
        $ValidationErrors += "Region '$RegionKey' is not a hashtable"
        continue
    }
    
    foreach ($ClassKey in $DocContent[$RegionKey].Keys) {
        $ClassDoc = $DocContent[$RegionKey][$ClassKey]
        
        if ($ClassDoc -isnot [hashtable]) {
            $ValidationErrors += "$RegionKey.$ClassKey is not a hashtable"
            continue
        }
        
        # Determine if enum or class based on keys
        $IsEnum = $ClassDoc.ContainsKey('ValueDescriptions')
        
        if ($IsEnum) {
            $EnumCount++
            
            # Validate enum structure
            $RequiredKeys = @('ShortDescription', 'LongDescription', 'ValueDescriptions', 'Examples', 'RelatedLinks', 'Notes')
            foreach ($Key in $RequiredKeys) {
                if (-not $ClassDoc.ContainsKey($Key)) {
                    $ValidationErrors += "$RegionKey.$ClassKey (enum) missing required key '$Key'"
                }
            }
            
            if ($ClassDoc.ContainsKey('ValueDescriptions') -and $ClassDoc.ValueDescriptions -isnot [hashtable]) {
                $ValidationErrors += "$RegionKey.$ClassKey.ValueDescriptions must be a hashtable"
            }
            
            if ($Detailed) {
                Write-Host "    $RegionKey.$ClassKey (enum) - OK" -ForegroundColor Gray
            }
        } else {
            $ClassCount++
            
            # Validate class structure
            $RequiredKeys = @('ShortDescription', 'LongDescription', 'PropertyDescriptions', 'MethodDescriptions', 'Examples', 'RelatedLinks', 'Notes')
            foreach ($Key in $RequiredKeys) {
                if (-not $ClassDoc.ContainsKey($Key)) {
                    $ValidationErrors += "$RegionKey.$ClassKey (class) missing required key '$Key'"
                }
            }
            
            if ($ClassDoc.ContainsKey('PropertyDescriptions') -and $ClassDoc.PropertyDescriptions -isnot [hashtable]) {
                $ValidationErrors += "$RegionKey.$ClassKey.PropertyDescriptions must be a hashtable"
            }
            
            if ($ClassDoc.ContainsKey('MethodDescriptions') -and $ClassDoc.MethodDescriptions -isnot [hashtable]) {
                $ValidationErrors += "$RegionKey.$ClassKey.MethodDescriptions must be a hashtable"
            }
            
            # Validate method descriptions structure
            if ($ClassDoc.ContainsKey('MethodDescriptions')) {
                foreach ($MethodKey in $ClassDoc.MethodDescriptions.Keys) {
                    $MethodDoc = $ClassDoc.MethodDescriptions[$MethodKey]
                    
                    if ($MethodDoc -isnot [hashtable]) {
                        $ValidationErrors += "$RegionKey.$ClassKey.MethodDescriptions.$MethodKey must be a hashtable"
                        continue
                    }
                    
                    $RequiredMethodKeys = @('Description', 'ReturnValue', 'Parameters', 'Example')
                    foreach ($Key in $RequiredMethodKeys) {
                        if (-not $MethodDoc.ContainsKey($Key)) {
                            $ValidationErrors += "$RegionKey.$ClassKey.MethodDescriptions.$MethodKey missing required key '$Key'"
                        }
                    }
                    
                    if ($MethodDoc.ContainsKey('Parameters') -and $MethodDoc.Parameters -isnot [hashtable]) {
                        $ValidationErrors += "$RegionKey.$ClassKey.MethodDescriptions.$MethodKey.Parameters must be a hashtable"
                    }
                }
            }
            
            if ($Detailed) {
                Write-Host "    $RegionKey.$ClassKey (class) - OK" -ForegroundColor Gray
            }
        }
        
        # Validate Examples structure
        if ($ClassDoc.ContainsKey('Examples') -and $ClassDoc.Examples -isnot [hashtable]) {
            $ValidationErrors += "$RegionKey.$ClassKey.Examples must be a hashtable"
        } elseif ($ClassDoc.ContainsKey('Examples')) {
            foreach ($ExampleKey in $ClassDoc.Examples.Keys) {
                $Example = $ClassDoc.Examples[$ExampleKey]
                if ($Example -isnot [hashtable]) {
                    $ValidationWarnings += "$RegionKey.$ClassKey.Examples.$ExampleKey should be a hashtable with Title/Code/Description"
                }
            }
        }
        
        # Validate RelatedLinks is array
        if ($ClassDoc.ContainsKey('RelatedLinks') -and $ClassDoc.RelatedLinks -isnot [array]) {
            $ValidationErrors += "$RegionKey.$ClassKey.RelatedLinks must be an array"
        }
        
        # Validate Notes is array
        if ($ClassDoc.ContainsKey('Notes') -and $ClassDoc.Notes -isnot [array]) {
            $ValidationErrors += "$RegionKey.$ClassKey.Notes must be an array"
        }
    }
}

if ($ValidationErrors.Count -eq 0) {
    Write-Host "  PASS: All structures valid ($ClassCount classes, $EnumCount enums)" -ForegroundColor Green
} else {
    Write-Host "  FAIL: Structure validation failed" -ForegroundColor Red
}

# Display results
Write-Host "`n=== Validation Summary ===" -ForegroundColor Cyan
Write-Host "  Classes:  $ClassCount" -ForegroundColor Gray
Write-Host "  Enums:    $EnumCount" -ForegroundColor Gray
Write-Host "  Errors:   $($ValidationErrors.Count)" -ForegroundColor $(if ($ValidationErrors.Count -eq 0) { 'Green' } else { 'Red' })
Write-Host "  Warnings: $($ValidationWarnings.Count)" -ForegroundColor $(if ($ValidationWarnings.Count -eq 0) { 'Green' } else { 'Yellow' })

if ($ValidationErrors.Count -gt 0) {
    Write-Host "`n=== Errors ===" -ForegroundColor Red
    foreach ($ValidationError in $ValidationErrors) {
        Write-Host "  - $ValidationError" -ForegroundColor Red
    }
}

if ($ValidationWarnings.Count -gt 0) {
    Write-Host "`n=== Warnings ===" -ForegroundColor Yellow
    foreach ($Warning in $ValidationWarnings) {
        Write-Host "  - $Warning" -ForegroundColor Yellow
    }
}

if ($ValidationErrors.Count -eq 0) {
    Write-Host "`n=== VALIDATION PASSED ===" -ForegroundColor Green
    return $true
} else {
    Write-Host "`n=== VALIDATION FAILED ===" -ForegroundColor Red
    return $false
}
