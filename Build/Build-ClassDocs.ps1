<#
.SYNOPSIS
    Generates comprehensive class documentation from Classes.psm1.

.DESCRIPTION
    Build-ClassDocs.ps1 parses the consolidated Classes.psm1 file and generates
    detailed markdown documentation for each class, organized by #region folders.
    
    This script runs rarely (when class structure changes significantly) and generates
    comprehensive documentation including:
    - Class overview and purpose
    - Properties with types and descriptions (placeholders for manual completion)
    - Methods with signatures, parameters, return types, and examples (placeholders)
    - Related links
    
    The folder structure mirrors the #region organization in Classes.psm1:
    - #region Enums -> docs/about/classes/Enums/
    - #region DRMMDevice and related classes -> docs/about/classes/DRMMDevice/
    
    The first word after #region becomes the folder name.

.PARAMETER OutputFolder
    Root folder for class documentation. Defaults to .\docs\about\classes

.PARAMETER Force
    Force regeneration of all class documentation.

.EXAMPLE
    .\Build\Build-ClassDocs.ps1
    Generates class documentation organized by region.

.EXAMPLE
    .\Build\Build-ClassDocs.ps1 -Force
    Regenerates all class documentation.

.NOTES
    Author: Robert Faddes
    This script is meant to be run infrequently when class structure changes.
    Generated files contain TODO placeholders for descriptions and examples.
#>
[CmdletBinding()]
param(
    [string]$OutputFolder = ".\docs\about\classes",
    [switch]$Force = $true
)

$ErrorActionPreference = 'Continue'

# Get module root
$ModuleRoot = Split-Path $PSScriptRoot -Parent
Push-Location $ModuleRoot

try {
    Write-Host "`n=== Build-ClassDocs.ps1 ===" -ForegroundColor Cyan
    Write-Host "Generating comprehensive class documentation from Classes.psm1..." -ForegroundColor Cyan
        # Load or initialize ClassDocContent.psd1
    $DocContentPath = Join-Path $PSScriptRoot 'ClassDocContent.psd1'
    if (Test-Path $DocContentPath) {
        Write-Host "Loading existing documentation content..." -ForegroundColor Yellow
        $DocContent = Import-PowerShellDataFile -Path $DocContentPath
    } else {
        Write-Host "No existing documentation content found. Will initialize new ClassDocContent.psd1" -ForegroundColor Yellow
        $DocContent = @{
            _Metadata = @{
                Version = '1.0.0'
                LastUpdated = Get-Date -Format 'yyyy-MM-dd'
                Description = 'Documentation content for DattoRMM.Core classes'
                AutoGenerated = $true
            }
        }
    }
        # Read manifest for DocsBaseUrl
    Write-Host "`nReading module manifest..." -ForegroundColor Yellow
    $ManifestPath = Join-Path $ModuleRoot 'DattoRMM.Core.psd1'
    $Manifest = Import-PowerShellDataFile -Path $ManifestPath
    $DocsBaseUrl = $Manifest.PrivateData.PSData.DocsBaseUrl
    if (-not $DocsBaseUrl) {
        Write-Warning "DocsBaseUrl not found in manifest. Using default."
        $DocsBaseUrl = 'https://github.com/TheShadowTek/DattoRMM.Core/blob/main/docs/'
    }
    Write-Host "  DocsBaseUrl: $DocsBaseUrl" -ForegroundColor Gray
        # Helper function to extract AST comments (returns synopsis only)
    function Get-ASTComment {
        param(
            [Parameter(Mandatory)]
            $MemberAst,
            [Parameter(Mandatory)]
            [string]$ScriptContent
        )
        
        # Tokenize the full script
        $tokens = [System.Management.Automation.PSParser]::Tokenize($ScriptContent, [ref]$null)
        
        # Find the token at the member's start line
        $memberStartLine = $MemberAst.Extent.StartLineNumber
        
        # Look backwards from the member for a comment block
        $relevantTokens = $tokens | Where-Object { 
            $_.StartLine -lt $memberStartLine 
        } | Sort-Object StartLine -Descending
        
        # Find the first comment before the member (could be multi-line)
        foreach ($token in $relevantTokens) {
            if ($token.Type -eq 'Comment') {
                # Check if this comment is immediately before the member (with possible blank lines)
                $gapLines = $memberStartLine - $token.EndLine
                if ($gapLines -le 2) {  # Allow up to 1 blank line between comment and member
                    # Extract and clean the comment
                    $commentText = $token.Content
                    
                    # Parse PowerShell help comment blocks
                    if ($commentText -match '(?s)<#(.*?)#>') {
                        $helpContent = $matches[1]
                        
                        # Extract .SYNOPSIS
                        if ($helpContent -match '(?s)\.SYNOPSIS\s+(.*?)(?=\s*\.[A-Z]|\s*#>|$)') {
                            $synopsis = $matches[1].Trim()
                            # Clean up multi-line synopsis
                            $synopsis = $synopsis -replace '\s+', ' '
                            return $synopsis
                        }
                    } elseif ($commentText -match '^#\s*(.+)') {
                        # Simple # comment
                        return $matches[1].Trim()
                    }
                }
                break
            }
        }
        
        return $null
    }
    
    # Helper function to extract full help comment (Synopsis and Description)
    function Get-ASTHelpComment {
        param(
            [Parameter(Mandatory)]
            $MemberAst,
            [Parameter(Mandatory)]
            [string]$ScriptContent
        )
        
        # Tokenize the full script
        $tokens = [System.Management.Automation.PSParser]::Tokenize($ScriptContent, [ref]$null)
        
        # Find the token at the member's start line
        $memberStartLine = $MemberAst.Extent.StartLineNumber
        
        # Look backwards from the member for a comment block
        $relevantTokens = $tokens | Where-Object { 
            $_.StartLine -lt $memberStartLine 
        } | Sort-Object StartLine -Descending
        
        $result = @{
            Synopsis = $null
            Description = $null
        }
        
        # Find the first comment before the member (could be multi-line)
        foreach ($token in $relevantTokens) {
            if ($token.Type -eq 'Comment') {
                # Check if this comment is immediately before the member (with possible blank lines)
                $gapLines = $memberStartLine - $token.EndLine
                if ($gapLines -le 2) {  # Allow up to 1 blank line between comment and member
                    # Extract and clean the comment
                    $commentText = $token.Content
                    
                    # Parse PowerShell help comment blocks
                    if ($commentText -match '(?s)<#(.*?)#>') {
                        $helpContent = $matches[1]
                        
                        # Extract .SYNOPSIS
                        if ($helpContent -match '(?s)\.SYNOPSIS\s+(.*?)(?=\s*\.[A-Z]|\s*#>|$)') {
                            $synopsis = $matches[1].Trim()
                            # Clean up multi-line synopsis
                            $result.Synopsis = $synopsis -replace '\s+', ' '
                        }
                        
                        # Extract .DESCRIPTION
                        if ($helpContent -match '(?s)\.DESCRIPTION\s+(.*?)(?=\s*\.[A-Z]|\s*#>|$)') {
                            $description = $matches[1].Trim()
                            # Clean up multi-line description
                            $result.Description = $description -replace '\s+', ' '
                        }
                    } elseif ($commentText -match '^#\s*(.+)') {
                        # Simple # comment - use as synopsis
                        $result.Synopsis = $matches[1].Trim()
                    }
                }
                break
            }
        }
        
        return $result
    }
    
    # Helper function to get doc content with fallback
    function Get-DocContent {
        param(
            [object]$Value,
            [string]$Default = "TODO: Add description"
        )
        
        if ($null -ne $Value -and $Value -ne '') {
            return $Value
        }
        return $Default
    }
        # Parse Classes.psm1
    Write-Host "`nParsing Classes.psm1..." -ForegroundColor Yellow
    $ClassesFile = Join-Path $ModuleRoot 'Private\Classes\Classes.psm1'
    
    if (-not (Test-Path $ClassesFile)) {
        Write-Error "Classes.psm1 not found at: $ClassesFile"
        return
    }
    
    $ClassesContent = Get-Content $ClassesFile -Raw
    Write-Host "  File size: $([math]::Round($ClassesContent.Length / 1KB, 2)) KB" -ForegroundColor Gray
    
    # Parse using AST (Abstract Syntax Tree)
    Write-Host "`nParsing with PowerShell AST..." -ForegroundColor Yellow
    $AST = [System.Management.Automation.Language.Parser]::ParseInput(
        $ClassesContent,
        [ref]$null,
        [ref]$null
    )
    
    # Extract #region information
    Write-Host "`nExtracting region structure..." -ForegroundColor Yellow
    $Regions = @()
    $Lines = $ClassesContent -split "`r?`n"
    
    for ($i = 0; $i -lt $Lines.Count; $i++) {
        $Line = $Lines[$i]
        if ($Line -match '^\s*#region\s+(.+)') {
            $RegionText = $matches[1].Trim()
            # First word becomes folder name
            $FolderName = ($RegionText -split '\s+')[0]
            $Regions += [PSCustomObject]@{
                Name = $RegionText
                FolderName = $FolderName
                StartLine = $i + 1  # 1-based
                Classes = @()
            }
            Write-Host "  Found region: $RegionText -> Folder: $FolderName" -ForegroundColor Gray
        }
    }
    
    Write-Host "  Found $($Regions.Count) regions" -ForegroundColor Gray
    
    # Find all class and enum definitions
    Write-Host "`nExtracting class definitions..." -ForegroundColor Yellow
    $TypeDefinitions = $AST.FindAll({
        param($node)
        $node -is [System.Management.Automation.Language.TypeDefinitionAst]
    }, $true)
    
    Write-Host "  Found $($TypeDefinitions.Count) type definitions (classes/enums)" -ForegroundColor Gray
    
    # Associate classes with regions based on line numbers
    foreach ($TypeDef in $TypeDefinitions) {
        $TypeName = $TypeDef.Name
        $TypeLine = $TypeDef.Extent.StartLineNumber
        $IsEnum = $TypeDef.IsEnum
        $IsClass = $TypeDef.IsClass
        
        # Find which region this type belongs to
        $AssignedRegion = $null
        for ($i = $Regions.Count - 1; $i -ge 0; $i--) {
            if ($TypeLine -ge $Regions[$i].StartLine) {
                $AssignedRegion = $Regions[$i]
                break
            }
        }
        
        if ($AssignedRegion) {
            Write-Host "    $TypeName ($(if($IsEnum){'enum'}else{'class'})) -> $($AssignedRegion.FolderName)" -ForegroundColor Gray
            
            # Extract detailed information
            $TypeInfo = [PSCustomObject]@{
                Name = $TypeName
                IsEnum = $IsEnum
                IsClass = $IsClass
                Properties = @()
                Methods = @()
                EnumValues = @()
                BaseType = $null
                ClassComment = $null
            }
            
            # Extract class/enum-level comment
            $HelpComment = Get-ASTHelpComment $TypeDef $ClassesContent
            $TypeInfo | Add-Member -NotePropertyName 'Synopsis' -NotePropertyValue $HelpComment.Synopsis -Force
            $TypeInfo | Add-Member -NotePropertyName 'Description' -NotePropertyValue $HelpComment.Description -Force
            
            if ($IsEnum) {
                # Extract enum values
                $TypeInfo.EnumValues = $TypeDef.Members | ForEach-Object {
                    $_.Name
                }
            } elseif ($IsClass) {
                # Extract base type if any
                if ($TypeDef.BaseTypes.Count -gt 0) {
                    $TypeInfo.BaseType = $TypeDef.BaseTypes[0].TypeName.Name
                }
                
                # Extract properties
                $Properties = $TypeDef.Members | Where-Object { 
                    $_ -is [System.Management.Automation.Language.PropertyMemberAst] 
                }
                
                $TypeInfo.Properties = foreach ($Prop in $Properties) {
                    $Comment = Get-ASTComment $Prop $ClassesContent
                    [PSCustomObject]@{
                        Name = $Prop.Name
                        Type = $Prop.PropertyType.TypeName.FullName
                        IsHidden = $Prop.IsHidden
                        Comment = $Comment
                    }
                }
                
                # Extract methods (excluding static FromAPIMethod)
                $Methods = $TypeDef.Members | Where-Object { 
                    $_ -is [System.Management.Automation.Language.FunctionMemberAst] 
                }
                
                $TypeInfo.Methods = foreach ($Method in $Methods) {
                    # Skip static FromAPIMethod (internal constructor)
                    if ($Method.IsStatic -and $Method.Name -eq 'FromAPIMethod') {
                        continue
                    }
                    
                    $Comment = Get-ASTComment $Method $ClassesContent
                    
                    # Get parameters
                    $Params = $Method.Parameters | ForEach-Object {
                        $ParamType = if ($_.StaticType) { 
                            $_.StaticType.Name 
                        } else { 
                            'object' 
                        }
                        $ParamName = $_.Name.VariablePath.UserPath
                        [PSCustomObject]@{
                            Name = $ParamName
                            Type = $ParamType
                        }
                    }
                    
                    $ReturnType = if ($Method.ReturnType) { 
                        $Method.ReturnType.TypeName.FullName 
                    } else { 
                        'void' 
                    }
                    
                    [PSCustomObject]@{
                        Name = $Method.Name
                        IsStatic = $Method.IsStatic
                        Parameters = $Params
                        ReturnType = $ReturnType
                        IsHidden = $Method.IsHidden
                        Comment = $Comment
                    }
                }
            }
            
            $AssignedRegion.Classes += $TypeInfo
        }
    }
        # Build class-to-folder lookup for inheritance links
    Write-Host "\nBuilding class-to-folder lookup..." -ForegroundColor Yellow
    $ClassFolderMap = @{}
    foreach ($Region in $Regions) {
        foreach ($TypeInfo in $Region.Classes) {
            $ClassFolderMap[$TypeInfo.Name] = $Region.FolderName
            Write-Host "  $($TypeInfo.Name) -> $($Region.FolderName)" -ForegroundColor Gray
        }
    }
    Write-Host "  Mapped $($ClassFolderMap.Count) classes to folders" -ForegroundColor Gray
    
    # Initialize/update documentation content for all classes
    Write-Host "\nInitializing/updating documentation content structure..." -ForegroundColor Yellow
    foreach ($Region in $Regions) {
        $RegionKey = $Region.FolderName
        
        if (-not $DocContent.ContainsKey($RegionKey)) {
            $DocContent[$RegionKey] = @{}
        }
        
        foreach ($TypeInfo in $Region.Classes) {
            $TypeName = $TypeInfo.Name
            
            if (-not $DocContent[$RegionKey].ContainsKey($TypeName)) {
                # New class/enum
                Write-Host "  Initializing content for: $RegionKey.$TypeName" -ForegroundColor Green
                
                if ($TypeInfo.IsEnum) {
                    $DocContent[$RegionKey][$TypeName] = @{
                        ShortDescription = $TypeInfo.Synopsis
                        LongDescription = $TypeInfo.Description
                        ValueDescriptions = @{}
                        Examples = @{}
                        RelatedLinks = @()
                        Notes = @()
                    }
                    
                    foreach ($Value in $TypeInfo.EnumValues) {
                        $DocContent[$RegionKey][$TypeName].ValueDescriptions[$Value] = $null
                    }
                } else {
                    $DocContent[$RegionKey][$TypeName] = @{
                        ShortDescription = $TypeInfo.Synopsis
                        LongDescription = $TypeInfo.Description
                        PropertyDescriptions = @{}
                        MethodDescriptions = @{}
                        Examples = @{}
                        RelatedLinks = @()
                        Notes = @()
                    }
                    
                    foreach ($Prop in $TypeInfo.Properties | Where-Object { -not $_.IsHidden }) {
                        if ($Prop.Name) {
                            $DocContent[$RegionKey][$TypeName].PropertyDescriptions[$Prop.Name] = $Prop.Comment
                        }
                    }
                    
                    foreach ($Method in $TypeInfo.Methods | Where-Object { -not $_.IsHidden }) {
                        if ($Method.Name) {
                            $MethodDoc = @{
                                Description = $Method.Comment
                                ReturnValue = $null
                                Parameters = @{}
                                Example = $null
                            }
                            foreach ($Param in $Method.Parameters) {
                                if ($Param.Name) {
                                    $MethodDoc.Parameters[$Param.Name] = $null
                                }
                            }
                            $DocContent[$RegionKey][$TypeName].MethodDescriptions[$Method.Name] = $MethodDoc
                        }
                    }
                }
            } else {
                # Existing class/enum - check for new/deprecated members and update AST comments
                $ExistingDoc = $DocContent[$RegionKey][$TypeName]
                
                # Update class-level descriptions if they're null/empty/placeholder and we have comments
                $shouldUpdateSynopsis = $TypeInfo.Synopsis -and (
                    -not $ExistingDoc.ShortDescription -or 
                    $ExistingDoc.ShortDescription -eq '' -or
                    $ExistingDoc.ShortDescription -match '^region ' -or
                    $ExistingDoc.ShortDescription -match 'related classes'
                )
                if ($shouldUpdateSynopsis) {
                    Write-Host "  Updating class synopsis for: $RegionKey.$TypeName" -ForegroundColor Magenta
                    $ExistingDoc.ShortDescription = $TypeInfo.Synopsis
                }
                
                $shouldUpdateDescription = $TypeInfo.Description -and (
                    -not $ExistingDoc.LongDescription -or 
                    $ExistingDoc.LongDescription -eq '' -or
                    $ExistingDoc.LongDescription -match '^The ' -and $ExistingDoc.LongDescription -match 'TODO'
                )
                if ($shouldUpdateDescription) {
                    Write-Host "  Updating class description for: $RegionKey.$TypeName" -ForegroundColor Magenta
                    $ExistingDoc.LongDescription = $TypeInfo.Description
                }
                
                if ($TypeInfo.IsClass) {
                    # Add new properties or update AST comments
                    foreach ($Prop in $TypeInfo.Properties | Where-Object { -not $_.IsHidden -and $_.Name }) {
                        if (-not $ExistingDoc.PropertyDescriptions.ContainsKey($Prop.Name)) {
                            Write-Host "  New property: $RegionKey.$TypeName.$($Prop.Name)" -ForegroundColor Cyan
                            $ExistingDoc.PropertyDescriptions[$Prop.Name] = $Prop.Comment
                        } elseif ($Prop.Comment -and (-not $ExistingDoc.PropertyDescriptions[$Prop.Name] -or $ExistingDoc.PropertyDescriptions[$Prop.Name] -eq '')) {
                            # Update null/empty descriptions with AST comments
                            Write-Host "  Updating AST comment for property: $RegionKey.$TypeName.$($Prop.Name)" -ForegroundColor Magenta
                            $ExistingDoc.PropertyDescriptions[$Prop.Name] = $Prop.Comment
                        }
                    }
                    
                    # Mark deprecated properties
                    $CurrentProps = @(($TypeInfo.Properties | Where-Object { -not $_.IsHidden -and $_.Name }).Name)
                    foreach ($PropName in @($ExistingDoc.PropertyDescriptions.Keys)) {
                        if ($PropName -notin $CurrentProps -and $PropName -notlike '_Deprecated_*') {
                            Write-Host "  Deprecated property: $RegionKey.$TypeName.$PropName" -ForegroundColor Yellow
                            $Value = $ExistingDoc.PropertyDescriptions[$PropName]
                            $ExistingDoc.PropertyDescriptions.Remove($PropName)
                            $ExistingDoc.PropertyDescriptions["_Deprecated_$PropName"] = $Value
                        }
                    }
                    
                    # Add new methods or update AST comments
                    foreach ($Method in $TypeInfo.Methods | Where-Object { -not $_.IsHidden -and $_.Name }) {
                        if (-not $ExistingDoc.MethodDescriptions.ContainsKey($Method.Name)) {
                            Write-Host "  New method: $RegionKey.$TypeName.$($Method.Name)()" -ForegroundColor Cyan
                            $MethodDoc = @{
                                Description = $Method.Comment
                                ReturnValue = $null
                                Parameters = @{}
                                Example = $null
                            }
                            foreach ($Param in $Method.Parameters) {
                                if ($Param.Name) {
                                    $MethodDoc.Parameters[$Param.Name] = $null
                                }
                            }
                            $ExistingDoc.MethodDescriptions[$Method.Name] = $MethodDoc
                        } elseif ($Method.Comment -and (-not $ExistingDoc.MethodDescriptions[$Method.Name].Description -or $ExistingDoc.MethodDescriptions[$Method.Name].Description -eq '')) {
                            # Update null/empty descriptions with AST comments
                            Write-Host "  Updating AST comment for method: $RegionKey.$TypeName.$($Method.Name)()" -ForegroundColor Magenta
                            $ExistingDoc.MethodDescriptions[$Method.Name].Description = $Method.Comment
                        }
                    }
                    
                    # Mark deprecated methods
                    $CurrentMethods = @(($TypeInfo.Methods | Where-Object { -not $_.IsHidden -and $_.Name }).Name)
                    foreach ($MethodName in @($ExistingDoc.MethodDescriptions.Keys)) {
                        if ($MethodName -notin $CurrentMethods -and $MethodName -notlike '_Deprecated_*') {
                            Write-Host "  Deprecated method: $RegionKey.$TypeName.$MethodName()" -ForegroundColor Yellow
                            $Value = $ExistingDoc.MethodDescriptions[$MethodName]
                            $ExistingDoc.MethodDescriptions.Remove($MethodName)
                            $ExistingDoc.MethodDescriptions["_Deprecated_$MethodName"] = $Value
                        }
                    }
                }
            }
        }
    }
    
    # Update metadata
    $DocContent['_Metadata'].LastUpdated = Get-Date -Format 'yyyy-MM-dd'
        
    # Generate markdown files organized by region
    Write-Host "`nGenerating markdown documentation..." -ForegroundColor Yellow
    
    $TotalGenerated = 0
    $TotalSkipped = 0
    
    foreach ($Region in $Regions) {
        if ($Region.Classes.Count -eq 0) {
            Write-Host "`n  Region '$($Region.FolderName)' has no classes, skipping..." -ForegroundColor Yellow
            continue
        }
        
        Write-Host "`n  Region: $($Region.Name)" -ForegroundColor Cyan
        Write-Host "  Folder: $($Region.FolderName)" -ForegroundColor Gray
        
        # Create region folder
        $RegionFolder = Join-Path $OutputFolder $Region.FolderName
        if (-not (Test-Path $RegionFolder)) {
            New-Item -Path $RegionFolder -ItemType Directory -Force | Out-Null
            Write-Host "    Created folder: $RegionFolder" -ForegroundColor Gray
        }
        
        foreach ($TypeInfo in $Region.Classes) {
            $TypeName = $TypeInfo.Name
            $MarkdownFile = Join-Path $RegionFolder "about_$TypeName.md"
            
            # Check if regeneration needed
            $ShouldGenerate = $Force
            if (-not $ShouldGenerate) {
                if (-not (Test-Path $MarkdownFile)) {
                    $ShouldGenerate = $true
                    $Reason = "new file"
                } elseif ((Get-Item $ClassesFile).LastWriteTime -gt (Get-Item $MarkdownFile).LastWriteTime) {
                    $ShouldGenerate = $true
                    $Reason = "source modified"
                }
            } else {
                $Reason = "forced"
            }
            
            if ($ShouldGenerate) {
                Write-Host "    Generating: about_$TypeName.md ($Reason)" -ForegroundColor Green
                
                # Build markdown content based on type
                if ($TypeInfo.IsEnum) {
                    # === ENUM DOCUMENTATION ===
                    $RegionKey = $Region.FolderName
                    $EnumDoc = $DocContent[$RegionKey][$TypeName]
                    
                    $ShortDesc = Get-DocContent $EnumDoc.ShortDescription "Describes the $TypeName enumeration used in DattoRMM.Core module"
                    $LongDesc = Get-DocContent $EnumDoc.LongDescription "The $TypeName enum defines valid values for TODO: describe what this enum represents"
                    
                    $Content = @"
# about_$TypeName

## SHORT DESCRIPTION

$ShortDesc

## LONG DESCRIPTION

$LongDesc

## VALUES

The following values are defined for $TypeName`:

| Value | Description |
|-------|-------------|
"@
                    foreach ($Value in $TypeInfo.EnumValues) {
                        $ValueDesc = Get-DocContent $EnumDoc.ValueDescriptions[$Value] "TODO: Describe this value"
                        $Content += "`n| ``$Value`` | $ValueDesc |"
                    }
                    
                    $Content += "`n`n## USAGE EXAMPLES`n`n"
                    if ($EnumDoc.Examples.Count -gt 0) {
                        $ExampleNum = 1
                        foreach ($ExampleKey in ($EnumDoc.Examples.Keys | Sort-Object)) {
                            $Example = $EnumDoc.Examples[$ExampleKey]
                            $Title = if ($Example.Title) { $Example.Title } else { "Example $ExampleNum" }
                            $Code = if ($Example.Code) { $Example.Code } else { "# TODO: Add example code" }
                            $Content += "### $Title`n`n``````powershell`n$Code`n``````\n`n"
                            $ExampleNum++
                        }
                    } else {
                        $Content += "### Example 1: Basic usage`n`n``````powershell`n# TODO: Add example showing how to use this enum`n``````\n`n"
                    }
                    
                    $Content += "## NOTES`n`n"
                    if ($EnumDoc.Notes.Count -gt 0) {
                        foreach ($Note in $EnumDoc.Notes) {
                            $NoteText = Get-DocContent $Note "Add a note"
                            $Content += "$NoteText`n`n"
                        }
                    } else {
                        $Content += "This enum is defined in the DattoRMM.Core module's Classes.psm1 file.`n`n"
                    }
                    
                    $Content += "## RELATED LINKS`n`n- [Online Documentation]($DocsBaseUrl/about/classes/$($Region.FolderName)/about_$TypeName.md)`n"
                    if ($EnumDoc.RelatedLinks.Count -gt 0) {
                        foreach ($Link in $EnumDoc.RelatedLinks) {
                            $Content += "- $Link`n"
                        }
                    }
                    $Content += "`n"
                } else {
                    # === CLASS DOCUMENTATION ===
                    $RegionKey = $Region.FolderName
                    $ClassDoc = $DocContent[$RegionKey][$TypeName]
                    
                    $ShortDesc = Get-DocContent $ClassDoc.ShortDescription "Add a brief description of this class"
                    $LongDesc = Get-DocContent $ClassDoc.LongDescription "Add a detailed description of what this class represents and its purpose"
                    
                    $BaseTypeText = if ($TypeInfo.BaseType) {
                        # Look up the folder for the base class
                        $BaseFolder = $ClassFolderMap[$TypeInfo.BaseType]
                        if ($BaseFolder) {
                            "This class inherits from [$($TypeInfo.BaseType)]($DocsBaseUrl/about/classes/$BaseFolder/about_$($TypeInfo.BaseType).md)."
                        } else {
                            # Fallback if base class not found in map
                            "This class inherits from $($TypeInfo.BaseType)."
                        }
                    } else {
                        ""
                    }
                    
                    $Content = @"
# about_$TypeName

## SHORT DESCRIPTION

$ShortDesc

## LONG DESCRIPTION

$LongDesc

$BaseTypeText

## PROPERTIES

The $TypeName class exposes the following properties:

"@
                    if ($TypeInfo.Properties.Count -gt 0) {
                        $Content += @"

| Property | Type | Description |
|----------|------|-------------|

"@
                        # Calculate consistent column widths (property name column)
                        $MaxPropNameLen = ($TypeInfo.Properties | ForEach-Object { $_.Name.Length } | Measure-Object -Maximum).Maximum
                        $MaxTypeLen = ($TypeInfo.Properties | ForEach-Object { $_.Type.Length } | Measure-Object -Maximum).Maximum
                        
                        foreach ($Prop in $TypeInfo.Properties | Where-Object { -not $_.IsHidden }) {
                            $PropName = $Prop.Name.PadRight($MaxPropNameLen)
                            $PropType = $Prop.Type.PadRight($MaxTypeLen)
                            $PropDesc = Get-DocContent $ClassDoc.PropertyDescriptions[$Prop.Name] "Add description"
                            $Content += "| $PropName | $PropType | $PropDesc |`n"
                        }
                    } else {
                        $Content += "`nNo public properties defined.\n"
                    }
                    
                    $Content += @"

## METHODS

The $TypeName class provides the following methods:

"@
                    
                    if ($TypeInfo.Methods.Count -gt 0) {
                        foreach ($Method in $TypeInfo.Methods | Where-Object { -not $_.IsHidden }) {
                            $ParamList = ($Method.Parameters | ForEach-Object {
                                "[$($_.Type)]`$$($_.Name)"
                            }) -join ', '
                            
                            $StaticText = if ($Method.IsStatic) { "static " } else { "" }
                            $Signature = "$StaticText$($Method.Name)($ParamList)"
                            
                            $MethodDoc = $ClassDoc.MethodDescriptions[$Method.Name]
                            $MethodDesc = Get-DocContent $MethodDoc.Description "Add method description explaining what this method does"
                            
