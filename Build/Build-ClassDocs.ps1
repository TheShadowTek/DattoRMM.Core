    function ConvertTo-Psd1String {
        param(
            [Parameter(Mandatory)]
            [object]$Object,
            [int]$IndentLevel = 0
        )
        
        $IndentStr = "    " * $IndentLevel
        $NextIndentStr = "    " * ($IndentLevel + 1)
        
        if ($Object -is [hashtable]) {
            $Lines = @("@{")
            
            foreach ($Key in ($Object.Keys | Sort-Object)) {
                $Value = $Object[$Key]
                
                if ($null -eq $Value) {
                    $Lines += "$NextIndentStr$Key = `$null"
                } else {
                    $ValueStr = ConvertTo-Psd1String -Object $Value -IndentLevel ($IndentLevel + 1)
                    $Lines += "$NextIndentStr$Key = $ValueStr"
                }
            }
            
            $Lines += "$IndentStr}"
            return ($Lines -join "`n")
        }
        
        if ($Object -is [array]) {
            if ($Object.Count -eq 0) {
                return "@()"
            }
            
            $Items = $Object | ForEach-Object {
                $ItemStr = ConvertTo-Psd1String -Object $_ -IndentLevel ($IndentLevel + 1)
                "$NextIndentStr$ItemStr"
            }
            
            return "@(`n" + ($Items -join "`n") + "`n$IndentStr)"
        }
        
        if ($Object -is [string]) {
            $Escaped = $Object -replace "'", "''"
            return "'$Escaped'"
        }
        
        if ($Object -is [bool]) {
            if ($Object) { return '$true' } else { return '$false' }
        }
        
        if ($null -eq $Object) {
            return '$null'
        }
        
        return "'$Object'"
    }
    
